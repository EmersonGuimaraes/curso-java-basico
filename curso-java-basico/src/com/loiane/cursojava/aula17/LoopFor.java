package com.loiane.cursojava.aula17;

public class LoopFor {

	public static void main(String[] args) {
		
		//For Normal
		
		for(int i=0; i <= 5; i++){
			System.out.println("O valor de i é "+i);
		}
		
		for(int i = 4; i >= 0; i--){
			System.out.println("O valor de i é "+i);
		}
		
		//For com mais de uma variável.
		
		for(int i = 0, j = 10; i < j; i++, j--){
			
		}
	}

}
